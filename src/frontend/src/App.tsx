const XP_THRESHOLDS = [0, 200, 500, 1000, 2000, 3500, 5500, 8000, 11000, 15000];

function getXP(): number {
  const val = Number.parseInt(localStorage.getItem("playerXP") ?? "0", 10);
  return Number.isNaN(val) ? 0 : val;
}

